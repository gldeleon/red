 <li class=MsoNormal style='text-align:justify;line-height:115%'><span
     style='font-family:"Calibri",sans-serif'>Si aparece el paciente continúe
     con el <span style='color:#830051'>paso 4.</span></span></li>
 <li class=MsoNormal style='text-align:justify;line-height:115%'><span
