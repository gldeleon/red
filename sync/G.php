<h1 style='text-align:justify;line-height:115%'><span style='font-family:"Calibri",sans-serif;
color:#7F7F7F'>PROCEDIMIENTO PARA ATENCIÓN DE PACIENTES</span></h1>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal style='text-align:justify;line-height:115%'><span
style='font-family:"Calibri",sans-serif'>Los pasos que deberá seguir cualquier
odontólogo que se encuentre dentro de Red Kobe para acreditar una atención
válida de pago serán los siguientes:</span></p>

<p class=MsoNormal style='text-align:justify;line-height:115%'><span
style='font-family:"Calibri",sans-serif'>&nbsp;</span></p>

<p class=MsoNormal style='margin-left:18.0pt;text-align:justify;text-indent:
-18.0pt;line-height:115%'><b><span style='font-family:"Calibri",sans-serif;
color:#830051'>1.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span></b><span style='font-family:"Calibri",sans-serif'>Recibir la
llamada de un paciente.</span></p>

<p class=MsoNormal style='margin-left:18.0pt;text-align:justify;text-indent:
-18.0pt;line-height:115%'><b><span style='font-family:"Calibri",sans-serif;
color:#830051'>2.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span></b><span style='font-family:"Calibri",sans-serif'>Solicitar el
nombre del paciente, aseguradora o empresa que le otorga el beneficio.</span></p>

<p class=MsoNormal style='margin-left:18.0pt;text-align:justify;text-indent:
-18.0pt;line-height:115%'><b><span style='font-family:"Calibri",sans-serif;
color:#830051'>3.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span></b><span style='font-family:"Calibri",sans-serif'>Busque al
paciente en la página del sistema de Red Kobe (</span><a
href="http://www.red.kobe.com.mx"><span style='font-family:"Calibri",sans-serif;
color:#ED7D31'>www.red.kobe.com.mx</span></a><span style='font-family:"Calibri",sans-serif'>)
en la sección de pacientes. Es importante cuidar la ortografía de escritura de
los nombres y apellidos como aparecen en la identificación. Asimismo le
recomendamos que utilice mezclas al escribir el nombre, por ejemplo, ingrese
solo los apellidos o primer nombre y dos apellidos, etc. Esto ayuda a eliminar
excepciones de búsqueda. </span></p>

