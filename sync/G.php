color:#830051'>4.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span></b><span style='font-family:"Calibri",sans-serif'>En el caso de
que el paciente no cumpliera con cobertura dental se atenderá como cualquier
paciente de primera vez en su consultorio. En otras palabras, es un paciente
particular.</span></p>

<p class=MsoNormal style='margin-left:18.0pt;text-align:justify;text-indent:
-18.0pt;line-height:115%'><b><span style='font-family:"Calibri",sans-serif;
color:#830051'>5.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span></b><span style='font-family:"Calibri",sans-serif'>Agende la cita
en el sistema.</span></p>

<ul style='margin-top:0cm' type=disc>
 <li class=MsoNormal style='text-align:justify;line-height:115%'><span
     style='font-family:"Calibri",sans-serif'>Recomendamos  confirmar  la  cita
     y asistencia del paciente un día antes para capturar en el sistema.</span></li>
</ul>

<p class=MsoNormal style='margin-left:18.0pt;text-align:justify;text-indent:
-18.0pt;line-height:115%'><b><span style='font-family:"Calibri",sans-serif;
color:#830051'>6.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span></b><span style='font-family:"Calibri",sans-serif'>Cuando el
paciente se presente en su clínica o consultorio el día y hora de su cita,
deberá solicitarle una identificación con fotografía así como la credencial del
seguro para validar que sea la misma persona.</span></p>

<p class=MsoNormal style='margin-left:35.4pt;text-align:justify;line-height:
115%'><span style='font-family:"Calibri",sans-serif;color:#830051'>IMPORTANTE</span><span
style='font-family:"Calibri",sans-serif'>. Le pedimos no penalizar a los
pacientes por inasistencias o retrasos. En el caso específico de retrasos
brinde opciones nuevas de cita, de ser posible el mismo día siempre que el
paciente pueda regresar o desee esperar.</span></p>

<p class=MsoNormal style='margin-left:18.0pt;text-align:justify;text-indent:
-18.0pt;line-height:115%'><b><span style='font-family:"Calibri",sans-serif;
color:#830051'>7.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span></b><span style='font-family:"Calibri",sans-serif'>Al paciente se
le proporcionará la historia clínica para que sea llenada (consulte el archivo
de Anexos). Le pedimos llenar el documento completamente y sin correcciones o
tachaduras. El llenado de expediente clínico es indispensable para respaldarlo
ante cualquier situación médica y legal, de acuerdo a la Norma Oficial Mexicana
NOM-168-SSA1-1998 del Expediente Clínico.</span></p>

<p class=MsoNormal style='margin-left:18.0pt;text-align:justify;text-indent:
-18.0pt;line-height:115%'><b><span style='font-family:"Calibri",sans-serif;
color:#830051'>8.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span></b><span style='font-family:"Calibri",sans-serif'>Cuando el
paciente ingresa a consulta, se capturará en el sistema inicio de sesión.</span></p>

<p class=MsoNormal style='margin-left:18.0pt;text-align:justify;text-indent:
-18.0pt;line-height:115%'><b><span style='font-family:"Calibri",sans-serif;
color:#830051'>9.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span></b><span style='font-family:"Calibri",sans-serif'>El odontólogo
revisará al paciente y realizará un diagnóstico.</span></p>

<p class=MsoNormal style='margin-left:18.0pt;text-align:justify;text-indent:
-18.0pt;line-height:115%'><b><span style='font-family:"Calibri",sans-serif;
color:#830051'>10.<span style='font:7.0pt "Times New Roman"'>&nbsp; </span></span></b><span
style='font-family:"Calibri",sans-serif'>En el sistema se capturará el
presupuesto por los tratamientos que requiere el paciente, incluyendo revisión;
se imprimirá en 2 juegos.</span></p>

<p class=MsoNormal style='margin-left:18.0pt;text-align:justify;text-indent:
-18.0pt;line-height:115%'><b><span style='font-family:"Calibri",sans-serif;
color:#830051'>11.<span style='font:7.0pt "Times New Roman"'>&nbsp; </span></span></b><span
style='font-family:"Calibri",sans-serif'>En caso de que los precios con
descuento en sistema Kobe sean superiores a los de su clínica y/o consultorio,
ofrezca el precio de su clínica y aplique el descuento que aparece en sistema
(o en presupuesto si ya lo imprimió). Si el precio de sistema es menor al de su
clínica, tome el precio de sistema. Esta media es fundamental para que el
paciente goce de un beneficio real por contratar su póliza.</span></p>

<p class=MsoNormal style='margin-left:18.0pt;text-align:justify;text-indent:
-18.0pt;line-height:115%'><b><span style='font-family:"Calibri",sans-serif;
color:#830051'>12.<span style='font:7.0pt "Times New Roman"'>&nbsp; </span></span></b><span
style='font-family:"Calibri",sans-serif'>Se le explicará al paciente el
presupuesto. Este momento es vital en la atención ya que determinará el plan de
tratamientos. Puede hacer uno o varios presupuestos hasta que logre un acuerdo
de un mix de tratamientos apropiado tanto para la salud como economía de sus
pacientes. Una vez aceptado deberá entregarle uno de los presupuestos impresos,
el otro deberá ser firmado tanto por el doctor que atiende como por el paciente
y ser incluido en el expediente.</span></p>

<p class=MsoNormal style='text-align:justify;line-height:115%'><span
style='font-family:"Calibri",sans-serif'>&nbsp;</span></p>

<p class=MsoNormal style='text-align:justify;line-height:115%'><span
style='font-family:"Calibri",sans-serif'>&nbsp;</span></p>

<p class=MsoNormal style='margin-left:18.0pt;text-align:justify;text-indent:
-18.0pt;line-height:115%'><b><span style='font-family:"Calibri",sans-serif;
color:#830051'>13.<span style='font:7.0pt "Times New Roman"'>&nbsp; </span></span></b><span
style='font-family:"Calibri",sans-serif'>Se inicia el o los tratamientos que
aceptó el paciente.</span></p>

<p class=MsoNormal style='margin-left:18.0pt;text-align:justify;text-indent:
-18.0pt;line-height:115%'><b><span style='font-family:"Calibri",sans-serif;
color:#830051'>14.<span style='font:7.0pt "Times New Roman"'>&nbsp; </span></span></b><span
style='font-family:"Calibri",sans-serif'>Ya que el o los tratamientos de esa
sesión hayan concluido se capturará la hora de salida en sistema.</span></p>

<p class=MsoNormal style='margin-left:18.0pt;text-align:justify;text-indent:
-18.0pt;line-height:115%'><b><span style='font-family:"Calibri",sans-serif;
