<h1><span style='font-family:"Calibri",sans-serif;color:#7F7F7F'>&nbsp;</span></h1>

<h1><span style='font-family:"Calibri",sans-serif;color:#7F7F7F'>OBJETIVO DE
ESTA POLÍTICA DE ATENCIÓN</span></h1>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal style='text-align:justify'><span style='font-family:"Calibri",sans-serif'>&nbsp;</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-family:"Calibri",sans-serif'>Red
Kobe busca y afilia a los mejores profesionales dentro de sus especialidades
odontológicas. Analiza las características técnicas y de servicio de cada uno.
Esto para brindar a nuestros pacientes la calidad y calidez que merecen al
momento de asistir al dentista.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-family:"Calibri",sans-serif'>&nbsp;</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-family:"Calibri",sans-serif'>Gracias
a ello, hoy somos la red dental mejor evaluada por sus pacientes, con el mínimo
número de quejas y mejor aún, con el mínimo número de incidencias clínicas.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-family:"Calibri",sans-serif'>&nbsp;</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-family:"Calibri",sans-serif'>Esto
solo se puede lograr con esfuerzo y disciplina de cada uno de sus integrantes.
La red es la suma del esfuerzo cada uno de ustedes y queremos continuar con
este mismo prestigio y nivel. </span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-family:"Calibri",sans-serif'>&nbsp;</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-family:"Calibri",sans-serif'>Es
por esto que hemos desarrollado esta política para garantizar nuestro objetivo.
Buscando además complementar nuestras atenciones con información de primera
calidad, que sea de utilidad en primer lugar para nuestros miembros y por ende
para los pacientes que atendemos.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-family:"Calibri",sans-serif'>&nbsp;</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-family:"Calibri",sans-serif'>De
antemano queremos agradecer su apoyo y seguimiento como hasta ahora ha sucedido
con todos los proyectos que lanzamos. Recuerde que el equipo de red siempre
está atento a sus necesidades a través de nuestros medios de contacto.</span></p>

